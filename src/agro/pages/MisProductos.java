      private void soloNumeros(JTextField j){
        
      
        j.addKeyListener(new KeyAdapter(){
            public void keyTyped (KeyEvent e){
                char c = e.getKeyChar();
                if(!Character.isDigit(c)){
                    e.consume();
                }  
            }
        });
    }
    
    public void cargarProductos(){
        MySQL miDB = new MySQL();
        miDB.MySQLConnection("root", "", "pruebaagro");
        
        String text = "";
        
        ArrayList<Producto> productos = new ArrayList<>();
        productos = miDB.getListProducto("producto", LoginPage.usuario.getEmail());
        
        
      System.out.println("productos "+ productos.isEmpty());
        
        if(productos != null){
            
            if(!productos.isEmpty()){
                
                for(Producto p : productos){
                     
                   text += "Cod: "+p.getCodigo()+" |  "+p.getNombreProducto()+"  |   $ "+String.valueOf(p.getPrecio())+"   |  Cantidad: "+p.getCantidad()+"\n";
                   text += p.getDescripcion()+"\n";
                   
                   text += "-------------------------------------------------------------------\n";
              
                    jTextArea1.setText(text);
                    jTextArea1.setEditable(false);
                    
                }
            }else{
                text = "Aún no has subido ningun producto ";
                jTextArea1.setText(text);
                jTextArea1.setEditable(false);
            }
        }else{
            System.out.println("Error list");
        }
        
        miDB.closeConnection();
            
        
        
        
    }


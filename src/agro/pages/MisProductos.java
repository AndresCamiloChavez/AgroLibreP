    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        MySQL miDB = new MySQL();
        miDB.MySQLConnection("root", "", "pruebaagro");
        
        String nombreTabla = "producto";
        String codigo = jTextField1.getText();
        
        boolean existe = miDB.verificarProductoUsuario(nombreTabla, codigo, usuario.getEmail());
        
        if(existe){
            miDB.deleteProducto(nombreTabla, codigo, usuario.getEmail());
            JOptionPane.showMessageDialog(this,"Se ha eliminado con exito!!! ", "Estado", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            cargarProductos();
            jTextArea1.updateUI();

        }else{
            JOptionPane.showMessageDialog(this,"ERROR, NO se ha podido eliminar. Intente de nuevo", "Estado", JOptionPane.ERROR_MESSAGE);

        }
        miDB.closeConnection();
        
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        MySQL miDB = new MySQL();
        miDB.MySQLConnection("root", "", "pruebaagro");
        
        String nombreTabla = "producto";
        String codigo = jTextField5.getText();
        
        
      
       
       if( miDB.verificarProductoUsuario(nombreTabla, codigo, usuario.getEmail())){
           
           
         Producto producto = miDB.getProducto("producto", codigo);

           
           jTextField2.setEnabled(true);
           jTextField3.setEnabled(true);
           jTextArea2.setEnabled(true);
           jTextField4.setEnabled(true);
           
           jTextField2.setText(producto.getNombreProducto());
           jTextField3.setText(String.valueOf(producto.getPrecio()));
           jTextField4.setText(String.valueOf(producto.getCantidad()));
           jTextArea2.setText(producto.getDescripcion());

           
           jButton3.setEnabled(true);

       }else{
        JOptionPane.showMessageDialog(this,"ERROR. No existe el producto. Intenta de nuevo", "Estado", JOptionPane.ERROR_MESSAGE);
        jTextField2.setEnabled(false);
           jTextField3.setEnabled(false);
           jTextArea2.setEnabled(false);
           jTextField4.setEnabled(false);
           
           jTextField2.setText("");
           jTextField3.setText("");
           jTextField4.setText("");
           jTextArea2.setText("");
       }
       miDB.closeConnection();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        MySQL miDB = new MySQL();
        miDB.MySQLConnection("root", "", "pruebaagro");
        String nombreTabla = "producto";
        String nombre =  jTextField2.getText();
        int cantidad = Integer.parseInt(jTextField4.getText());
        double precio = (!jTextField3.getText().isEmpty())? Double.parseDouble(jTextField3.getText()):0.0;
        double sinPrecio = 0.0;
        String descripcion = jTextArea2.getText();
        String codigo = jTextField5.getText();

        
        
        if(miDB.verificarProductoUsuario(nombreTabla, codigo, usuario.getEmail())){
            
             if( !nombre.isEmpty() && precio != sinPrecio && cantidad != 0){
              int estatus = miDB.updateProducto(nombreTabla, nombre, codigo,usuario.getEmail(),precio,cantidad,descripcion);
           
            if(estatus == 1){
                 jTextField2.setText("");
                 jTextField3.setText("");
                 jTextField4.setText("");
                 jTextArea2.setText("");
                 

                 jTextField2.setEnabled(false);
                 jTextField3.setEnabled(false);
                 jTextField4.setEnabled(false);
                 jButton3.setEnabled(false);
                jTextArea2.setEnabled(false);

                 
                 cargarProductos();
                 jTextArea1.updateUI();
                JOptionPane.showMessageDialog(this,"Se actualizo con exito!! ", "Estado", JOptionPane.INFORMATION_MESSAGE);

            }else{
                JOptionPane.showMessageDialog(this,"ERROR. No se pudo actualizar con exito ", "Estado", JOptionPane.ERROR_MESSAGE);
                
            }
          }else{
              JOptionPane.showMessageDialog(this,"ERROR. Campos vacios.Intenta de nuevo", "Estado", JOptionPane.ERROR_MESSAGE);

             }
        }else{
              jTextField2.setText("");
              jTextField3.setText("");
              jTextField4.setText("");
              jTextArea2.setText("");
              JOptionPane.showMessageDialog(this,"ERROR. No existe el producto. Intenta de nuevo", "Estado", JOptionPane.ERROR_MESSAGE);
              jTextField2.setEnabled(false);
              jTextField3.setEnabled(false);
              jTextField4.setEnabled(false);
             jTextArea2.setEnabled(false);

              jButton3.setEnabled(false);

        }
        
        miDB.closeConnection();
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

